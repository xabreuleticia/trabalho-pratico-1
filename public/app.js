const cards = document.getElementById("cards");

if(cards){

    // pega o nome da página atual
    const pagina = window.location.pathname;

    let receitasFiltradas = receitas;

    // filtros por página
    if(pagina.includes("acompanhamentos.html")){

        receitasFiltradas = receitas.filter(receita =>
            receita.tipo == "acompanhamento"
        );
    }

    else if(pagina.includes("lanches.html")){

        receitasFiltradas = receitas.filter(receita =>
            receita.tipo == "lanche"
        );
    }

    else if(pagina.includes("sobremesas.html")){

        receitasFiltradas = receitas.filter(receita =>
            receita.tipo == "sobremesa"
        );
    }

    else if(pagina.includes("almoco&jantar.html")){

        receitasFiltradas = receitas.filter(receita =>
            receita.tipo == "almoço e jantar"
        );
    }

    // renderiza os cards
    receitasFiltradas.forEach(receita => {

        cards.innerHTML += `

        <div class="col-md-4 col-12 mb-4">

            <a href="detalhes.html?id=${receita.id}" class="card-receita">

                <article class="receita">

                    <img 
                        src="${receita.imagem}" 
                        alt="${receita.nome}"
                        class="img-receita"
                    >

                    <h4 class="subtitulo mt-3">
                        ${receita.nome}
                    </h4>

                    <p class="Texto">
                        Tempo: ${receita.tempo}
                    </p>

                </article>

            </a>

        </div>
        `;
    });
}

